export const generateChartData = () => {
  const data = [];
  let basePrice = 0.60;
  const now = Date.now();
  
  for (let i = 100; i >= 0; i--) {
    const timestamp = now - (i * 3600000); // hourly data
    const volatility = (Math.random() - 0.5) * 0.02;
    basePrice = Math.max(0.3, Math.min(0.85, basePrice + volatility));
    
    data.push({
      time: timestamp,
      price: basePrice,
      volume: Math.random() * 10000 + 5000
    });
  }
  
  return data;
};
